## agora proponho o seguinte, vamos guardar em uma variável um nome, lembrando
## que o método input() retorna um texto, logo, basta atribuir a uma variável.
## não importa se tem espaços, caractéres especiais ou qualquer outra coisa.
## lembre-se de dar bons nomes às variáveis, que facilitem a leitura do código.

## assim que nossa variável estiver feita, vamos criar uma outra variável e
## guardar um sobrenome. Lembrando que essas informações devem ser obtidas com
## o método input().

## e agora vamos realizar uma concatenção, isso é, juntar textos.
## e faremos assim: nome_completo = nome + sobrenome

## é isso mesmo, parece ser uma soma, mas já que estamos trabalhando com duas
## variáveis do tipo String(texto), é uma concatenação.

## peça o nome, o sobrenome, e imprima no terminal o nome completo

## -------- ESCREVA O CÓDIGO AQUI ABAIXO E EXECUTE -----------------

nome = input('Digite o seu primeiro nome: ')
sobrenome = input('Digite aqui o seu sobrenome: ')

nome_completo = nome + ' ' + sobrenome

print(nome_completo)



